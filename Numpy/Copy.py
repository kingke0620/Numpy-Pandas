import numpy as np
a = np.arange(4)
print(a,'\n--------------')
b = a
c = a
d = b
a[0] = 11#修改数据的时候要保证类型相同，此处不能用float型
print(a,'\n--------------')
print(b,'\n--------------')
print(c,'\n--------------')
print(d,'\n--------------')
'''
这里仅仅是赋给了引用，并非具体的值
同理，如果对bcd进行操作，a也会改变
'''
d[1:3] = [22,33]
print(d,'\n--------------')
print(a,'\n--------------')

#*************仅仅赋值
b = a.copy()#deep copy
a[3] = 44
print(a,'\n--------------')
print(b,'\n--------------')
#观察是否关联，看看地址就可以了
print(id(a) == id(b))
